public class Attributes {
    char A;
    char B;
    char C;

    public Attributes() {
        A = 'A';
        B = 'B';
        C = 'C';
    }

    public char getA() {
        return A;
    }

    public void setA(char a) {
        A = a;
    }


    public char getB() {
        return B;
    }

    public void setB(char b) {
        B = b;
    }

    public char getC() {
        return C;

    }

    public void setC(char c) {
        C = c;
    }

}
